/* Operadores lógicos

Valores booleanos, quando verificados, resultarão em verdadeiro ou falso.
*/

let pao = true;
let queijo = true;

//AND && - os dois valores têm que ser verdadeiro pra resposta ser verdadeira
console.log(pao && queijo);

//OR || - único caso de falsidade é quando os dois valores forem falsos
console.log(pao || queijo);

//NOT ! - negação
console.log(!pao && queijo);
